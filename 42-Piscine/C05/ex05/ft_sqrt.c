int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	while ((a * a) != nb)
	{
		if (a > 46341)
			return (0);
		a++;
	}
	return (a);
}
